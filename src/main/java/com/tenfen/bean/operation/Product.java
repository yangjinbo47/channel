package com.tenfen.bean.operation;

public class Product {
	private String ssoAddr;
	private String sid;
	private String spid;
	private String monthProductId;
	private String tmpidp;
	private String tmpidsp;
	private String token;
	public String getSsoAddr() {
		return ssoAddr;
	}
	public void setSsoAddr(String ssoAddr) {
		this.ssoAddr = ssoAddr;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getSpid() {
		return spid;
	}
	public void setSpid(String spid) {
		this.spid = spid;
	}
	public String getMonthProductId() {
		return monthProductId;
	}
	public void setMonthProductId(String monthProductId) {
		this.monthProductId = monthProductId;
	}
	public String getTmpidp() {
		return tmpidp;
	}
	public void setTmpidp(String tmpidp) {
		this.tmpidp = tmpidp;
	}
	public String getTmpidsp() {
		return tmpidsp;
	}
	public void setTmpidsp(String tmpidsp) {
		this.tmpidsp = tmpidsp;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
}
